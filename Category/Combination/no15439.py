# 문제 
'''
베라는 상의 N벌과 하의 N벌이 있다.
i번째 상의와 i번째 하의는 모두 색상 i를 가진다.
N개의 생상은 모두 서로 다르다.

상의와 하의가 서로 다른 색상인 조합은 총 몇 가지 일까?
'''

# 입력
'''
첫째 줄에 입력 N이 주어진다. (1 <= N <= 2017, N은 정수)
'''

# 출력
'''
상의와 하의가 서로 다른 색상인 조합의 가짓수를 출력한다.
'''

N = int(input("N: "))

fassion = [(i,j) for i in range(0,N) for j in range(0,N-1)]

print(len(fassion))