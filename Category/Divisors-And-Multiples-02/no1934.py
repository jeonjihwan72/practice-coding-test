# 유클리드 호제법 (Euclidean Algorithm)

# 정의 
'''
- 두 정수의 최대공약수를 구하는 알고리즘으로, 두 수를 나눈 나머지를 이용해 반복적으로 계산하는 방식
- 원리: 두 정수 a와 b(a > b)가 있을 때, a를 b로 나눈 나머지를 r이라 하면, gcd(a, b) = gcd(b, r)가 성립.
- 이 과정을 나머지가 0이 될 때까지 반복하면, 마지막 b 값이 두 수의 최대공약수가 됨.
'''

# 알고리즘
'''
1. 두 수 a, b 중 큰 수를 a, 작은 수를 b로 둔다.
2. a를 b로 나눈 나머지 r을 구한다.
3. r이 0이면, b가 최대공약수이다.
4. r이 0이 아니면, a=b, b=r로 변경한 후 2번 과정부터 반복한다.

코드 구현
def gcd(a, b):
    while b != 0:
        a, b = b, a % b
    return a
'''

# 장점
'''
1. 효율성
    - 두 수의 크기에 관계없이 빠르게 최대공약수를 계산
    - 시간 복잡도: O(log(min(a,b))) → 큰 수에서도 빠르게 수행

2. 간결한 구현
    - 몇 줄의 코드로 쉽게 구현할 수 있음

3. 다양한 활용도
    - 최소공배수(LCM) 계산, 분수의 약분, 암호화 알고리즘(예: RSA) 등 여러 곳에 사용됨

4. 메모리 효율성
    - 추가적인 메모리 사용없이 재귀나 반복문만으로 수행 가능
'''

# 단점
'''
1. 연산 횟수 증가 가능성
    - 입력값 중 하나가 매우 큰 수일 경우, 반복 연산 횟수가 많아질 수 있음
      (하지만 여전히 O(log n) 이므로 큰 문제는 아님)

2. 음수, 0 처리 필요
    - 입력값이 음수이거나 0일 경우 코드에서 별도 처리가 필요함
    
3. 소수(Prime) 처리
    - 두 수가 소수일 경우, 거의 끝까지 연산이 진행되므로 상대적으로 연산량이 많아짐.
      (하지만 O(log n)이라 실질적 큰 문제는 없음)
'''

